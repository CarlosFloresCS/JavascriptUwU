// undefined indica que no se ha inicializado
// una variable y que el valor esta ausente
// dado por javascript
let indefinida;
console.log(indefinida); // undefined

// null es un valor especial que indica
// la ausencia de valor
// generado por el programador
let nulo = null;
console.log(nulo); // null

// NaN Not a Number
let noEsUnNumero = "hola" * 3.7;
console.log(noEsUnNumero) // NaN

